class WorkoutObject {
    constructor(name, calories, duration, category, exercises, date, notes, id) {
        this.name = name;
        this.calories = calories;
        this.duration = duration;
        this.category = category;
        this.exercises = exercises;
        this.date = date;
        this.notes = notes;
        this.id = id;
    }
}

export default WorkoutObject;

